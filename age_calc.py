name = input("Введіть ім'я: ")  # вводження ім,я
years = int(input("Введіть роки: "))  # вводження років

month = years * 12  # виводження місяців де ми множим на 12
days = years * 365  # виводження в днів множим на 365

print(f"Ім,я  {name}")  # ім'я яке ми просили в користувачв
print(f"Роки {years}")  # Роки які ми теж просили
print(f"Місяці:  {month}")  # Місяці  роки які ми множили на 12
print(f"Днів: {days}")  # Дні які де роки множились на 365
