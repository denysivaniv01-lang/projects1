A = {1, 2, 3, 4, 5}
B = {3, 4, 5, 6, 7}
res = A - B
print(f"Результат не має однакових елементів (A):{res}") # A-B

res2 = B - A
print(f"Результат не має однакових елементів (B):{res2}") # B-A

final_res = res ^ res2 # елементи що не є спільними для обох множин
print(f"Виводим елементи які не спільні для них обох:{final_res}")