# Завдання 2: Зворотній відлік від 10 до 1 за допомогою range(10, 0, -1)
# Третій аргумент (-1) вказує крок ітерації він зменшує значення на 1 при кожному кроці.
for a in range(10, 0, -1):
    print(a)